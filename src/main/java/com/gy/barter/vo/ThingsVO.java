package com.gy.barter.vo;

public class ThingsVO {

	private String price;
	private String des;
	private String have;
	private String want;
	private String condition;
	
	private String people_name;
	
	private String people_tel;
	
	private String people_qq;
	
	
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getDes() {
		return des;
	}
	public void setDes(String des) {
		this.des = des;
	}
	public String getHave() {
		return have;
	}
	public void setHave(String have) {
		this.have = have;
	}
	public String getWant() {
		return want;
	}
	public void setWant(String want) {
		this.want = want;
	}
	public String getCondition() {
		return condition;
	}
	public void setCondition(String condition) {
		this.condition = condition;
	}
	public String getPeople_name() {
		return people_name;
	}
	public void setPeople_name(String people_name) {
		this.people_name = people_name;
	}
	public String getPeople_tel() {
		return people_tel;
	}
	public void setPeople_tel(String people_tel) {
		this.people_tel = people_tel;
	}
	public String getPeople_qq() {
		return people_qq;
	}
	public void setPeople_qq(String people_qq) {
		this.people_qq = people_qq;
	}
	
	
	
}
